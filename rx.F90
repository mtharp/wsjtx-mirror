subroutine rx

!  Receive and decode MEPT_JT signals for one 2-minute sequence.

#ifdef CVF
  use dfport
#else
  integer time
#endif

  integer soundin
  include 'acom1.f90'

  npts=114*12000
  if(ncal.eq.1) npts=65536
  if(ncal.eq.3) npts=nrecsec*12000
  nsec1=time()
  f0a=f0                                   !Save rx frequency at start
  ierr=soundin(ndevin,kwave,4*npts)
  if(ierr.ne.0) then
     print*,'Error in soundin',ierr
     stop
  endif
  call fil1(kwave,4*npts,iwave,n2)
  npts=n2
  nsec2=time()
  call getrms(iwave,npts,ave,rms)          !### is this needed any more??
  call cs_lock('rx')
  nrxdone=1
  if(ncal.eq.1) ncal=2
  if(ncal.eq.3) ncal=4
  call cs_unlock

  return
end subroutine rx

